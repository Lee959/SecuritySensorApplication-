package com.example.securitysensorapplication;

public class DeviceTypeCode {
    // Temperature and humidity sensor
    public static final int TH_SENSOR = 0x0302;

    // Light sensor
    public static final int LX_SENSOR = 0x0106;

    // Smoke detector
    public static final int SMOKE_SENSOR_ZONE = 0x0500;

    // Motion sensor
    public static final int MOTION_SENSOR_ZONE = 0x0406;

    // IR controller
    public static final int AC_SENSOR = 0x0201;

    // Alarm
    public static final int WARN_SENSOR = 0x0403;

    // Curtain motor
    public static final int WARN_MOTOR = 0x0202;

    // Door sensor
    public static final int DOOR_SENSOR = 0x0402;
}